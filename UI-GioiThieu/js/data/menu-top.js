function getMenu(li) {
  let obj = "";
  let aTaglink = li.querySelector("a");

  let temp = `"${aTaglink.innerText.trim()}":{link:"${aTaglink.href}",`;
  //submenu
  let subUl = li.querySelector("ul");
  let submenu = "";
  if (subUl) {
    let listLi = subUl.childNodes;
    for (let li of listLi) {
      submenu += `${getMenu(li)}`;
    }
  }
  temp += submenu.length == 0 ? `},` : `submenu:{${submenu}}},`;
  obj += temp;
  return obj;
}
